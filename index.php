			</h4><br>
			<h4 style="font-family: 'Pridi', serif;"> 
				  นอกจากนี้ยังเป็นการฝึกทักษะการนำเสนอผลงานทางวิชาการ ทักษะการสื่อสาร อีกทั้งยังจัดให้มีการบรรยายพิเศษจากผู้ทรงคุณวุฒิ<br>รับเชิญ   และในแต่ละปีได้มีการผลัดเปลี่ยนหมุนเวียนให้แต่ละสถาบันการศึกษาที่ร่วมโครงการเป็นเจ้าภาพหลักในการจัดโครงการประชุมวิชาการ<br>
			  ดังกล่าว    ซึ่งโครงการนี้มีการดำเนินการอย่างต่อเนื่องมาจนถึงปี พ.ศ. 2561 (ปีการศึกษา 2560) นับรวมเป็นครั้งที่ 6   ในการนี้สาขาวิชาสถิติ   <br>
			  คณะวิทยาศาสตร์  มหาวิทยาลัยแม่โจ้  ซึ่งเป็นสถาบันเจ้าภาพหลักในการจัดโครงการประชุมวิชาการประจำปี พ.ศ. 2561  ได้ยกระดับโครงการจากประชุมวิชาการและการนำเสนอผลงานของนักศึกษาเพียง 3 สถาบันเป็นการจัด  "ประชุมวิชาการระดับปริญญาตรี  สาขาวิชาสถิติ  ระดับชาติ"
			</h4><br>
			<h4 style="font-family: 'Pridi', serif;">
